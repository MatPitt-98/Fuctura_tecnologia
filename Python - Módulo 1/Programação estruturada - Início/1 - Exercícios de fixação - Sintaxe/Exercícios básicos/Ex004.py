# Crie um programa que leia o número de horas trabalhadas por um funcionário e o valor da hora de trabalho para calcular seu salário bruto.

horas = float(input('Digite quantas horas você trabalhou: '))
valor_hora = float(input('Digite o valor da hora de trabalho: '))
print(f'Seu salário bruto é {horas * valor_hora}. ')